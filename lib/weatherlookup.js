
var request = require('superagent');

var Lookup = module.exports = function (apiKey) {
  this.endpoint =
    'https://api.forecast.io/forecast/';
  this.apiKey = apiKey;
};

Lookup.prototype.getWeather = function (latitude, longitude, callback) {
  if (!longitude || !latitude) {
    return callback(new Error('Missing longitude/lattitude data'));
  }

  var requestData = '/' + latitude + ',' + longitude;

  request
    .get(this.endpoint + this.apiKey + requestData)
    .set('Accept', 'application/json')
    .end(function (err, res) {
      if (err) {
        return callback(err);
      }

      var data = res.body || {};

      if (!data.currently) {
        return callback(new Error('Invalid response from weather api'));
      }

      return callback(undefined, data);
    });
};

